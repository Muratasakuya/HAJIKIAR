#include "ICommand.h"

/*////////////////////////////////////////////////////////////////////////////////
*								  移動処理 左
////////////////////////////////////////////////////////////////////////////////*/
void MoveLeftCommand::Exec(GameObject3D& object) {

	object.MoveLeft();
}

/*////////////////////////////////////////////////////////////////////////////////
*								  移動処理 右
////////////////////////////////////////////////////////////////////////////////*/
void MoveRightCommand::Exec(GameObject3D& object) {

	object.MoveRight();
}